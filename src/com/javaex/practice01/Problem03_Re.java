public class Problem03_Re {

	public static void main(String[] args){
		
		for(int row=1; row<10; row++){
			for(int col=2; col<10; col++ ){
			System.out.print(col + "*" + row + "=" + row*col);
			System.out.print("\t");
			}
			System.out.println();
			}
	}
	}

